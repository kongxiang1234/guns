package cn.stylefeng.guns.sys.core.constant.state;



/**
 * 部门类型
 *
 * @author fengshuonan
 * @Date 2017年1月22日 下午12:14:59
 */

public enum DeptType {

    BENBU("本部"),
    FENBU("分部"),
    FENGONGSI("分公司"),
    BUMEN("部门");


    private String type;

    DeptType(String type) {
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
